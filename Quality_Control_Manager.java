public class Quality_Control_Manager extends User{
    protected int id;
    public String name;

    public Quality_Control_Manager() {
    }

    public Quality_Control_Manager(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Quality_Control_Manager{" + "id=" + id + ", name=" + name + '}';
    }



}
