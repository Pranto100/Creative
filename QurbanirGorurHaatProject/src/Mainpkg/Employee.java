package Mainpkg;

import java.time.LocalDate;

/**
 *
 * @author User
 */
public class Employee extends User {
    protected int empID;
    LocalDate doj;
    protected double salary;

    public Employee(int empID, LocalDate doj, double salary, int id, int password, String gender, String contact, String address) {
        super(id, password, gender, contact, address);
        this.empID = empID;
        this.doj = doj;
        this.salary = salary;
    }

    public int getEmpID() {
        return empID;
    }

    public void setEmpID(int empID) {
        this.empID = empID;
    }

    public LocalDate getDoj() {
        return doj;
    }

    public void setDoj(LocalDate doj) {
        this.doj = doj;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPassword() {
        return password;
    }

    public void setPassword(int password) {
        this.password = password;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "Employee{" + "empID=" + empID + ", doj=" + doj + ", salary=" + salary + '}';
    }
    
    
}
